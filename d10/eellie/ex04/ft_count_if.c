int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int cn;

	i = 0;
	cn = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) == 1)
			cn++;
		i++;
	}
	return (cn);
}
