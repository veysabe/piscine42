int		ft_strcmp(char *s1, char *s2)
{
	int cn;

	cn = 0;
	while (s1[cn] != '\0' && s2[cn] != '\0')
	{
		if (s1[cn] != s2[cn])
		{
			return (s1[cn] - s2[cn]);
		}
		cn++;
	}
	return (s1[cn] - s2[cn]);
}
